#include <iostream>
#include <string.h>
#include <string>

using namespace std;


char array[10];
int main() {
	
	int t = 1;
	while(true)
	{
		scanf("%s", &array);
		if(array[0] == '*')
		{
			break;
		}
		else
		{
			if(array[0] == 'H')
			{
				printf("Case %d: Hajj-e-Akbar\n", t);
			}
			else
			{
				printf("Case %d: Hajj-e-Asghar\n", t);
			}
			t++;
		}
	}
	
	return 0;
}
